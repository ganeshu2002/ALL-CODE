public class basics {
    public static void main(String[] args) {
    // pre incrementoperator and post increment operator
    
    int x = 5;

    System.out.println(x++);  // post incre -> print then update 
    System.out.println(x);

    System.out.println(++x);  // pre incre -> update then print
    System.out.println(x);


  


    
    
    }
}
