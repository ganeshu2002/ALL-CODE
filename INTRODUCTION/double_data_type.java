import java.time.Year;

public class double_data_type {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double z = x/y;
        System.out.println(z);
        
        double u = 5/2;
        System.out.println(u);

        int f = 5/2;
        System.out.println(f);

        double s = 5.0/2;
        System.out.println(s);

        double st = 5/2.0;
        System.out.println(st);

        

    }
}
